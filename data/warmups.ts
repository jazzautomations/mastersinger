// Helper: build a 5-note scale pattern (do-re-mi-fa-sol-fa-mi-re-do) on a root.
function fiveNoteScale(rootMidi: number, beatMs = 600) {
  const intervals = [0, 2, 4, 5, 7, 5, 4, 2, 0];
  return {
    midis: intervals.map(i => rootMidi + i),
    beatMs,
  };
}

